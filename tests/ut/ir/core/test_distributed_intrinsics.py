"""Tests for distributed intrinsic Call nodes (Step 06).

Distributed primitives are represented as Call nodes with 'dist.*' op names.
These are primarily generated by the lowering pass (Step 07), not user-written.
"""

import pytest
from pypto.pypto_core import DataType, ir


def _span():
    return ir.Span("test", 1, 0)


def _make_var(name):
    return ir.Var(name, ir.ScalarType(DataType.INT32), _span())


# ─── dist.* Call node creation ────────────────────────────────────────────


def test_dist_make_tensor_call():
    """dist.make_tensor Call node can be created."""
    op = ir.Op("dist.make_tensor")
    rt_var = _make_var("rt")
    count = ir.ConstInt(1024, DataType.INT32, _span())
    call = ir.Call(op, [rt_var, count], _span())
    assert call.op.name == "dist.make_tensor"
    assert len(call.args) == 2


def test_dist_tree_reduce_call():
    """dist.tree_reduce Call node can be created."""
    op = ir.Op("dist.tree_reduce")
    rt_var = _make_var("rt")
    leaves_var = _make_var("leaves")
    call = ir.Call(op, [rt_var, leaves_var], _span())
    assert call.op.name == "dist.tree_reduce"
    assert len(call.args) == 2


def test_dist_submit_worker_call():
    """dist.submit_worker Call node can be created."""
