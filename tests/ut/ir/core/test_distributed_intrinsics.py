# ─── structural_equal with dist.* calls ──────────────────────────────────


def test_dist_call_structural_equal():
    """Two dist.* Call nodes with same op are structurally equal (no args)."""
    op1 = ir.Op("dist.tree_reduce")
    op2 = ir.Op("dist.tree_reduce")
    c1 = ir.Call(op1, [], _span())
    c2 = ir.Call(op2, [], _span())
    ir.assert_structural_equal(c1, c2)


def test_dist_call_different_op_not_equal():
    """Two dist.* Call nodes with different ops are not equal."""
    op1 = ir.Op("dist.tree_reduce")
    op2 = ir.Op("dist.submit_worker")
    c1 = ir.Call(op1, [], _span())
    c2 = ir.Call(op2, [], _span())
    with pytest.raises(ValueError):
        ir.assert_structural_equal(c1, c2)


if __name__ == "__main__":
    pytest.main([__file__, "-v"])
